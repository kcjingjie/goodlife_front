<template>
  <div class="page-header-index-wide page-header-wrapper-grid-content-main">
    <a-row :gutter="24">
      <a-col :md="24" :lg="7">
        <a-card :bordered="false">
          <div class="account-center-avatarHolder">
            <div class="avatar">
              <img :src="userInfo.imgUrl">
            </div>
            <div class="bio">{{userInfo.username}}</div>
          </div>
          <div class="account-center-detail">
            <p>
              <i class="group"></i>创建时间 {{userInfo.createTime}}
            </p>
          </div>
          <a-divider/>
          <a-divider :dashed="true"/>
        </a-card>
      </a-col>
      <a-col :md="24" :lg="17">
        <a-card
          style="width:100%"
          :bordered="false"
          :tabList="tabListNoTitle"
          :activeTabKey="noTitleKey"
          @tabChange="key => handleTabChange(key, 'noTitleKey')"
        >
          <videos-page v-if="noTitleKey === 'video'" :id="$route.params.id"></videos-page>
          <fans-page v-else-if="noTitleKey === 'fans'"></fans-page>
        </a-card>
      </a-col>
    </a-row>
  </div>
</template>

<script>
import { VideosPage , FansPage, ProjectPage } from './page'
import {getUserInfoById} from '@/api/user'
import { mapGetters } from 'vuex'

export default {
  components: {
    VideosPage,
    FansPage,
    ProjectPage
  },
  data () {
    return {

      userInfo:{
        username:'',
        imgUrl:'',
        createTime:''
      },
      tagInputValue: '',

      teams: [],
      teamSpinning: true,

      tabListNoTitle: [
        {
          key: 'video',
          tab: '视频'
        },
        {
          key: 'fans',
          tab: '粉丝(8)'
        },{
          key:'project',
          tab:'文章'
      }
      ],
      noTitleKey: 'video'
    }
  },
  mounted () {
    var id = this.$route.params.id
    getUserInfoById({id:id}).then(res=>{
        this.userInfo = res.data
    })
  },
  methods: {
    ...mapGetters(['nickname', 'avatar']),


    handleTabChange (key, type) {
      this[type] = key
    },

    handleTagClose (removeTag) {
      const tags = this.tags.filter(tag => tag !== removeTag)
      this.tags = tags
    },

    showTagInput () {
      this.tagInputVisible = true
      this.$nextTick(() => {
        this.$refs.tagInput.focus()
      })
    },

    handleInputChange (e) {
      this.tagInputValue = e.target.value
    },

    handleTagInputConfirm () {
      const inputValue = this.tagInputValue
      let tags = this.tags
      if (inputValue && !tags.includes(inputValue)) {
        tags = [...tags, inputValue]
      }

      Object.assign(this, {
        tags,
        tagInputVisible: false,
        tagInputValue: ''
      })
    }
  }
}
</script>

